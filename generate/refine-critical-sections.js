function normalizeText(text) {
  return String(text || "").replace(/\s+/g, " ").trim();
}

async function main() {
  const tafsir = JSON.parse(await fs.readFile(TAFSIR_PATH, "utf8"));

  for (const item of updates) {
    const pageEntry = tafsir[String(item.page)];
    if (!pageEntry || !Array.isArray(pageEntry.tafsir_sections) || !pageEntry.tafsir_sections[item.index - 1]) {
      continue;
    }
    pageEntry.tafsir_sections[item.index - 1].tafsir = normalizeText(item.text);
  }

  await fs.writeFile(TAFSIR_PATH, `${JSON.stringify(tafsir, null, 2)}\n`, "utf8");
  console.log(`Refined sections: ${updates.length}`);
}

main().catch((err) => {
  console.error("Error:", err.message || err);
  process.exitCode = 1;
});
